#include <stddef.h>

void	*gc_memsetd(void *b, double c, size_t len)
{
	size_t	i;
	double	*bcpy;

	bcpy = b;
	i = 0;
	while (i < len)
	{
		bcpy[i] = c;
		i++;
	}
	return (b);
}
